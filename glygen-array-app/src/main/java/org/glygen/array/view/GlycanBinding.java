package org.glygen.array.view;

public class GlycanBinding {
	
	String glycanId;
	Double bindingValue;
	
	/**
	 * @return the glycanId
	 */
	public String getGlycanId() {
		return glycanId;
	}
	/**
	 * @param glycanId the glycanId to set
	 */
	public void setGlycanId(String glycanId) {
		this.glycanId = glycanId;
	}
	/**
	 * @return the bindingValue
	 */
	public Double getBindingValue() {
		return bindingValue;
	}
	/**
	 * @param bindingValue the bindingValue to set
	 */
	public void setBindingValue(Double bindingValue) {
		this.bindingValue = bindingValue;
	}
	
	

}
